    private void DetenerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DetenerActionPerformed
        // TODO add your handling code here:
        // Bot√≥n para detener el juego
        //se detiene los metdos de las figuras al mismo tiempo
        //con una metedo llamado detener
        
        
        for (int i = 1; i < figura.length; i++) {
            figura[i].detener();
        }
        
        //se va mostrar lo que se ha conseguido
        int num;
        num = maquina.contarFigurasIguales(figura);
        
       maquina.puntajeGlobal += num;
        switch (num) {
            case 1000 -> JOptionPane.showMessageDialog(null, "Felicitaciones, has ganado! \n"+ num +"   puntos");
            case 300 -> JOptionPane.showMessageDialog(null, "Felicitaciones, has Consegudo 2 figuras! \n"+num+"   puntos");
            default -> JOptionPane.showMessageDialog(null, "Inteero de nuevo, has Consegudo 1 figuras! \n"+num+"   puntos");
        }
    }//GEN-LAST:event_DetenerActionPerformed

